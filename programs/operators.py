#Arithmatic operators
num1 = 8
num2 = 3
print("add", num1 + num2) #11
print("sub", num1 - num2) #5
print("mul", num1 * num2) #24
print("div", num1 / num2) #2.666
print("mod", num1 % num2) #2

#relational operators
print(num1 == num2) #false
print(num1 != num2) #true
#print(num1 <> num2) <> this is not supported
print(num1 <= num2) #false
print( num1 >= num2) #true

#the concate operator
str1 ='wow ' + 'python'
print(str1)


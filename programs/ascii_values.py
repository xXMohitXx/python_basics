#to find the ascii values ord() and chr() functions are used
print(ord('A'))
print(ord('a'))
print(ord('B'))
print(ord('b'))

print(chr(65))
print(chr(97))
print(chr(66))
print(chr(98))
print(chr(32))

#difference between ascii values
print(ord('a'), '-', ord('A'), '=', end=' ')
print(ord('a') - ord('A'), end=' ') #97 - 65 = 32
